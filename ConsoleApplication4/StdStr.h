/*  StdStr.H  */
//
// 字符串辅助函数库
// 提供简单的字符串长度计算工具，不依赖标准库 strlen。
// 主要用于引擎内部字体输出时计算文本像素宽度。
//

// -------------------------------------

/**
 * @brief 返回字符串的字节长度（包含所有字符，含控制字符）
 *
 * 从字符串起始位置逐字节遍历，直到遇到 '\0' 终止符。
 * 等价于标准库的 strlen()。
 *
 * @param Str 指向以 '\0' 结尾的字符串的指针
 * @return 字符串的总字节数（不含 '\0'）
 */
long StrLen(char *Str)
{
	long i=0;

	while(*(Str+i)) i++;
	return i;
}

// -------------------------------------

/**
 * @brief 返回字符串中可显示字符的数量（排除控制字符）
 *
 * 遍历字符串，遇到换行符 '\n' 立即停止并返回当前计数。
 * 跳过 ASCII 码 <= 31 的控制字符（如 '\r'、'\t' 等），只统计
 * 可见字符（ASCII >= 32）的个数。
 *
 * 用途：在计算文本绘制宽度时，只统计真正占用屏幕像素的字符数，
 * 以便居中或右对齐时准确计算偏移量。
 *
 * @param Str 指向以 '\0' 结尾的字符串的指针
 * @return 可见字符（ASCII > 31）的数量；如遇 '\n' 则返回该行前的可见字符数
 */
long StrLenEx(char *Str)
{
	long	i=0, t=0;
	BYTE	c;

	while(*(Str+i))
	{
		c=*(Str+i);
		if(c=='\n') return t;   // 遇到换行符，停止计数并返回
		i++;
		if(c>31) t++;           // 只统计可见字符（ASCII 32 及以上）
	}
	return t;
}

