/**
 * 在指定位置输出字符串（内部版本）
 * 
 * 支持换行符'\n'，换行时Y增加7像素，X回到起始位置
 * 每个字符宽度6像素
 * 
 * @param X, Y - 输出起始位置的左上角坐标
 * @param str - 要输出的字符串
 */
void _OutTextXY(long X, long Y, char *str)
{
	long	i, x;
	BYTE 	c;

	i = 0; x = X;
	while((c = str[i ++]) > 0)
	{
		// 处理换行符
		if(c == '\n')
		{
			Y += 7;				// 换行，Y增加（行高7像素）
			X = x;				// X回到行首
		}
		else
		{
			// 输出字符，成功后X增加6（字符宽度）
			if(_OutCharXY(X, Y, c)) X += 6;
		}
	}
}

