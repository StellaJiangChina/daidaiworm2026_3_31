// 字符绘制函数
// ========================================

/**
 * 在指定位置输出单个字符（内部版本）
 * 
 * 使用 _PSet 函数绘制像素（可能是直接写显存版本）
 * 字符宽度6像素，高度8像素
 * 
 * @param X, Y - 输出位置的左上角坐标
 * @param ASC - 要输出的ASCII字符（必须 >= 32）
 * @return TRUE成功，FALSE失败（控制字符）
 */
BOOL _OutCharXY(long X, long Y, char ASC)
{
	long 	x, y, offset;
	BYTE 	mask;

	// 控制字符（<32）不绘制
	if(ASC < 32) return FALSE;
	
	// 计算字符在字体数组中的偏移量
	// 每个字符8字节，ASCII-32得到字符索引
	offset = (ASC - 32 ) << 3;
	
	// 遍历8行
	for(y = 0; y < 8; y ++)
	{
		mask=SYSFONT[offset+y];		// 获取该行的位图数据
		
		// 遍历6列（每字节的高6位有效）
		for(x = 0; x < 6; x ++)
		{
			// 检查最高位，如果为1则绘制像素
			if(mask > 0x7F)
			{
				_PSet(X + x, Y + y);
			}
			mask <<= 1;				// 左移，检查下一位
		}
	}
	return TRUE;
}

// ========================================

/**
 * 在指定位置输出单个字符（标准版本）
 * 
 * 使用 PSet 函数绘制像素（可能是带裁剪检查版本）
 * 功能与 _OutCharXY 相同，但使用不同的底层绘制函数
 * 
 * @param X, Y - 输出位置的左上角坐标
 * @param ASC - 要输出的ASCII字符（必须 >= 32）
 * @return TRUE成功，FALSE失败（控制字符）
 */
BOOL OutCharXY(long X, long Y, char ASC)
{
	long 	x, y, offset;
	BYTE 	mask;

	if(ASC < 32) return FALSE;
	offset = (ASC - 32 ) << 3;
	for(y = 0; y < 8; y ++)
	{
		mask=SYSFONT[offset+y];
		for(x = 0; x < 6; x ++)
		{
			if(mask > 0x7F)
			{
				PSet(X + x, Y + y);
			}
			mask <<= 1;
		}
	}
	return TRUE;
}

