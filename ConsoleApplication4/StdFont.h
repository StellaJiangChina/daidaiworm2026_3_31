/**
 * 在指定位置输出字符串（标准版本）
 * 
 * 功能与 _OutTextXY 相同，但使用 OutCharXY 绘制字符
 * 支持换行符处理
 * 
 * @param X, Y - 输出起始位置的左上角坐标
 * @param str - 要输出的字符串
 */
void OutTextXY(long X, long Y, char *str)
{
	long	i, x;
	BYTE 	c;

	i = 0; x = X;
	while((c = str[i ++]) > 0)
	{
		if(c == '\n')
		{
			Y += 7;
			X = x;
		}
		else
		{
			if(OutCharXY(X, Y, c)) X += 6;
		}
	}
}
